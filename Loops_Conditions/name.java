package Loops_Conditions;
import java.util.*;
public class name {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter Your Name:-");
        int n = sc.nextInt();
        for(int i =1; i<=n; i++){
            System.out.println(i+ " " + "Krishna");
            sc.close();
        }
    }  
}
    